/**
 * op_add - calculates the sum of two integers
 * @a: first integer
 * @Xal: second integer
 *
 * Return: sum of a and Xal
 */
int op_add(int a, int Xal)
{
	return (a + Xal);
}

/**
 * op_sub - calculates the difference of two integers
 * @a: first integer
 * @Xal: second integer
 *
 * Return: difference of a and Xal
 */
int op_sub(int a, int Xal)
{
	return (a - Xal);
}

